/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
 var reverseList = function(head) {
    let prev = null;
    let next_node;
    let curr = head;
    while(curr !== null){
        next_node = curr.next;
        curr.next = prev;
        prev = curr;
        curr = next_node;
    }
    return prev;
};